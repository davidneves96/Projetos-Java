package pOO;

public class Operacoes {

	
	//metodo de somar dois numeros
	public static int somar (int num1, int num2) {
		return num1 + num2;
	}
	
	public int subtrair (int num1, int num2) {
		return num1 - num2;
	}

	public float dividir (float num1, float num2) {
		return num1 / num2;
	}
}
